#数据类型
#整数
a = 1;
print("整数",a);
#浮点数
b = 1.01;
print("浮点数",b);
#字符串
print("字符串","I\'m ok.");
#布尔值
print("布尔值",3>5);
#空值  是Python里一个特殊的值，用None表示。None不能理解为0，因为0是有意义的，而None是一个特殊的空值
print(None);
#常量 常量就是不能变的变量，比如常用的数学常数π就是一个常量。在Python中，通常用全部大写的变量名表示常量
PI = 3.14159265359;
print("常量",PI);
#计算
print(10 / 3);
print(9 / 3);
print(10 // 3);
print(10 % 3);